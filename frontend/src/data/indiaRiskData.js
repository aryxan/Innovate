// Helper function to get risk color
export const getRiskColor = (risk) => {
  switch (risk) {
    case "high":
      return "#ef4444"; // Red
    case "medium":
      return "#eab308"; // Yellow
    case "low":
      return "#22c55e"; // Green
    default:
      return "#6b7280"; // Gray
  }
};
